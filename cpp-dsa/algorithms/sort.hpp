/**
 * Bubble sort algorithm that sorts an array of integers in ascending order,
 * in place.
 * 
 * @param arr[] array to be sorted.
 * @param size size of the array.
*/
void bubbleSort(int arr[], int size);


/**
 * Selection sort algorithm that sorts an array of integers in ascending order,
 * in place.
 * 
 * @param arr[] array to be sorted.
 * @param size size of the array
*/
void selectionSort(int arr[], int size);


/**
 * Insertion sort algorithm that sorts an array of integers in ascending order,
 * in place.
 * 
 * @param arr[] array to be sorted.
 * @param size size of the array
*/
void insertionSort(int arr[], int size);
